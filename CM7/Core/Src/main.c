void i2c_write(uint8_t reg, uint8_t data)
{
  uint8_t bytes[2] = {reg, data};

  i2c_flag = 0;
  if(HAL_I2C_Master_Transmit_IT(&hi2c2, 0x58, bytes, 2) != HAL_OK) {
    Error_Handler();
  }
  while(i2c_flag == 0) {}
}

void i2c_read(uint8_t reg, uint8_t *data)
{

  i2c_flag = 0;
  if(HAL_I2C_Master_Transmit_IT(&hi2c2, 0x58, &reg, 1) != HAL_OK) {
    Error_Handler();
  }
  while(i2c_flag == 0) {}
  i2c_flag = 0;
  if(HAL_I2C_Master_Receive_IT(&hi2c2, 0x58, data, 1) != HAL_OK) {
    Error_Handler();
  }
  while(i2c_flag == 0) {}
}

